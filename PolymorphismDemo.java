class Polymorphism{
    public static int concatinate(int...a){
        int sum =0;
        for(int i:a){
            sum=sum+i;
        }
        return sum;
    }
    public static double concatinate(double...a){
        double sum =0;
        for(double i:a){
            sum=sum+i;
        }
        return sum;
    }

    public static String concatinate(String...n){
        String s="";
        for(String i:n){
            s=s+i+" ";
        }
        return s;

    }
}

public class PolymorphismDemo {
    public static void main(String args[]){
        
        System.out.println(Polymorphism.concatinate(1,2,3,4));
        System.out.println(Polymorphism.concatinate(1.8,2.9));
        System.out.println(Polymorphism.concatinate("A","B","C"));

    }
}
